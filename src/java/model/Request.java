package model;

/**
 *
 * @author Hung
 */
public class Request {
    int id;
    String name;
    String author;
    int userId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public Request(int id, String name, String author, int userId) {
        this.id = id;
        this.name = name;
        this.author = author;
        this.userId = userId;
    }

    public Request() {
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }
    
}
